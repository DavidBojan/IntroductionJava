public class DataTypes {
    public static void main(String[] args) {
        // most used data types

        // String = sequence of characters represented inside double quotes
        String brand = "Nissan";
        String model = "GTR";
        // upper case function
        System.out.println(brand.toUpperCase());
        // length function
        System.out.println(model.length());

        // integer = primitive data type, positive or negative non-decimal number
        int fabrication_year = 1990;

        // double = decimal number
        double price = 18.000;

        // boolean = true or false. 1 or 0
        boolean registered = true;

        // char =  single character
        char grade = 'A';
    }
}
